% FUNCTION TO ALTER A IRREGULAR VERB
% REPLACE VERB WITH PROPER IRREGULAR VERB FORM BASED OFF OF CONJUGATION
change2(Number, Person, Mood, Verb, Determiner, Return) :-
    verb(End, [Verb, Number, Person, Mood, Determiner]),
    string_chars(Return, End).



% FUNCTION TO ALTER A REGULAR VERB
% REMOVE THE LAST TWO LETTERS AND APPEND PROPER FORM BASED OFF OF CONJUGATION
change(Verblist, Number, Person, Mood, Verb, Determiner, Return) :-
    reverse(Verblist, [_,_|Temp]),
    reverse(Temp, Root),
    verb(End, [Verb, Number, Person, Mood, Determiner]),
    append(Root, End, R),
    string_chars(Return, R).

% CHECKS TO FIND OUT IF VERB IS IRREGULAR 
% USE ATTRIBUTES LIKE DETERMINER, TYPE, NUMBER, PERSON, AND MOOD
% RETURN ANSWER IN THE RETURN VARIABLE
portuguese(Verb, Determiner, Type, Number, Person, Mood, Return) :-
    noun(Noun, [Determiner, Number, determiner]),
    verb(_, [Verb, irregular]),
    Type = irregular,
    change2(Number, Person, Mood, Verb, Determiner, X),
    string_chars(StrN, Noun),
    atom_concat(StrN, ' ', Temp),
    atom_concat(Temp, X, Return).

